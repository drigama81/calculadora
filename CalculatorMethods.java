public class CalculatorMethods {

    public int sum(int algarismoA, int algarismoB) {
        int soma = algarismoA+algarismoB;
        return soma;
    }

    public int sub(int algarismoA, int algarismoB) {
        int subtracao = algarismoA-algarismoB;
        return subtracao;
    }

    public int div(int algarismoA, int algarismoB) {
        int divisao = algarismoA/algarismoB;
        return divisao;
    }

    public int mult(int algarismoA, int algarismoB) {
        int multiplicacao = algarismoA*algarismoB;
        return multiplicacao;
    }
    
    


}
